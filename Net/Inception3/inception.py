class Inception_yolo(nn.Module):
    def __init__(self,feature,class_num=1,is_cuda=False):
        super(Inception_yolo, self).__init__()
        self.is_cuda = is_cuda
        self.features=feature
        self.class_num=class_num

        self.extra_conv=nn.Sequential(
            torch.nn.Conv2d(2048,1024,3,stride=1,padding=0),
            torch.nn.BatchNorm2d(num_features=1024),
            nn.ReLU(inplace=True),
            torch.nn.MaxPool2d(kernel_size=2,stride=2),

            torch.nn.Conv2d(1024,1024,1,stride=1,padding=0),
            torch.nn.BatchNorm2d(num_features=1024),
            nn.ReLU(inplace=True),
        )

        self.fc_layer= nn.Sequential(
            nn.Linear(7*7*1024, 2048),
            nn.ReLU(inplace=True),
            nn.Dropout(0.5),
            nn.Linear(2048, 7*7*(self.class_num+5*2)),
            nn.Sigmoid(),
        )

        self._initialize_weights()



    def forward(self,x):
        conv_feature = self.features(x)
        conv_feature = self.extra_conv(conv_feature)

        result=conv_feature.view(conv_feature.size(0),-1)
        result = self.fc_layer(result)
        result = result.view(result.size(0),self.class_num+5*2,7,7)


        # very dirty code ... try to add offset to output,since our output is relative to the grid left-up corner[0.0~1.0]
        # ----- -----------------------------------------------------------------------------
        a=np.array([0,1,2,3,4,5,6])
        a = np.reshape(a,[1,7])
        a= np.tile(a,[7,1])
        a = a[np.newaxis,...]
        a=np.tile(a,[2,1,1])

        b=np.array([0,1,2,3,4,5,6])
        b = np.reshape(b,[7,1])
        b= np.tile(b,[1,7])
